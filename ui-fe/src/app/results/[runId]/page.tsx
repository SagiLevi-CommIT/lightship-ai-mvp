'use client';

import { useCallback, useEffect, useMemo, useRef, useState } from 'react';
import { useParams, useRouter } from 'next/navigation';
import AppShellHeader from '@/components/evaluation/app-shell-header';
import BatchNotification from '@/components/evaluation/batch-notification';
import EvaluationReportResults from '@/components/evaluation/evaluation-report-results';
import BackendFrameGallery from '@/components/evaluation/backend-frame-gallery';
import ResultsPropertiesPanel from '@/components/evaluation/results-properties-panel';
import { useEvaluationFlow } from '@/components/evaluation/flow-provider';
import {
  downloadFramesZipUrl,
  getClientConfigs,
  getFrames,
  getOutputJson,
  getVideoClass,
  retry404,
  type FrameManifest,
  type VideoClassInfo,
} from '@/lib/api';
import { backendToPipelineResultJson, pipelineResultToPropertyRows } from '@/lib/map-backend-to-template';
import type { AssetResult } from '@/components/evaluation/flow-types';

type ResultTab = 'frames' | 'rendered' | 'json';
type SeverityFilter = 'all' | 'high' | 'medium' | 'low';

const DEFAULT_EXPORT_PATH = 's3://lightship-mvp-processing-336090301206/results';

const WINDOW_THRESHOLD = 40;
const WINDOW_PAGE_SIZE = 40;

type EnrichedAssetResult = AssetResult & {
  jobId?: string;
  frames?: FrameManifest | null;
  videoClass?: VideoClassInfo | null;
};

// Heuristic: true for "run_<ms>" run IDs generated by the UI;
// otherwise treat the path segment as a backend job_id (UUID) and try
// to fetch it directly.
const looksLikeBackendJobId = (runId: string) =>
  /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(runId);

async function fetchBackendResult(jobId: string): Promise<EnrichedAssetResult | null> {
  try {
    const [bv, configs, frames, videoClass] = await Promise.all([
      retry404(() => getOutputJson(jobId)),
      retry404(() => getClientConfigs(jobId)).catch(() => null),
      retry404(() => getFrames(jobId)).catch(() => null),
      retry404(() => getVideoClass(jobId)).catch(() => null),
    ]);
    const rawJson = backendToPipelineResultJson(bv, configs);
    return {
      assetId: jobId,
      assetName: bv.filename,
      previewUrl: '',
      kind: 'video',
      rawJson,
      propertyRows: pipelineResultToPropertyRows(rawJson),
      jobId,
      frames,
      videoClass,
    };
  } catch (e) {
    console.error('fetchBackendResult failed', e);
    return null;
  }
}

export default function ResultsPage() {
  const params = useParams<{ runId: string }>();
  const router = useRouter();
  const { requestNotificationPermission, resetFlow, setNotificationMessage, state } = useEvaluationFlow();

  const historicalRun = state.historicalRuns.find((run) => run.runId === params.runId) ?? null;
  const isCurrentRun = state.currentRunId === params.runId;

  const activeMode = isCurrentRun ? state.mode : historicalRun?.mode ?? null;
  const inMemoryEntries = useMemo(
    () => Object.values(isCurrentRun ? state.resultsByAssetId : historicalRun?.resultsByAssetId ?? {}),
    [historicalRun?.resultsByAssetId, isCurrentRun, state.resultsByAssetId],
  );

  // Fallback when neither the current run nor a historical run matches:
  // fetch the runId straight from the backend (assume it's a job_id).
  const [fetchedEntries, setFetchedEntries] = useState<Array<EnrichedAssetResult>>([]);
  const [fetchState, setFetchState] = useState<'idle' | 'loading' | 'ok' | 'error'>('idle');

  useEffect(() => {
    if (inMemoryEntries.length > 0) {
      setFetchedEntries([]);
      setFetchState('ok');
      return;
    }
    if (!params.runId) {
      setFetchState('idle');
      return;
    }
    if (!looksLikeBackendJobId(params.runId)) {
      // Not a job_id and no history — legitimate "not found".
      setFetchState('error');
      return;
    }
    let cancelled = false;
    setFetchState('loading');
    (async () => {
      const res = await fetchBackendResult(params.runId);
      if (cancelled) return;
      if (res) {
        setFetchedEntries([res]);
        setFetchState('ok');
      } else {
        setFetchState('error');
      }
    })();
    return () => {
      cancelled = true;
    };
  }, [inMemoryEntries.length, params.runId]);

  const resultEntries: Array<EnrichedAssetResult> = useMemo(
    () => (inMemoryEntries.length > 0
      ? (inMemoryEntries as Array<EnrichedAssetResult>)
      : fetchedEntries),
    [fetchedEntries, inMemoryEntries],
  );

  const [selectedAssetId, setSelectedAssetId] = useState<string | null>(resultEntries[0]?.assetId ?? null);
  const [activeTab, setActiveTab] = useState<ResultTab>('frames');
  const [visibleCount, setVisibleCount] = useState<number>(WINDOW_PAGE_SIZE);
  const [showBatchCompletionPopup, setShowBatchCompletionPopup] = useState<boolean>(
    activeMode === 'batch' && isCurrentRun,
  );
  const [zipBusy, setZipBusy] = useState<boolean>(false);
  // Post-run filter / export controls (Bug 5 — these used to live in
  // the pre-run sidebar, which was wrong because both are only
  // meaningful after results exist).
  const [severityFilter, setSeverityFilter] = useState<SeverityFilter>('all');
  const [exportPath, setExportPath] = useState<string>(DEFAULT_EXPORT_PATH);
  const [exportBusy, setExportBusy] = useState<boolean>(false);
  const [exportMessage, setExportMessage] = useState<string | null>(null);
  const listRef = useRef<HTMLDivElement | null>(null);

  // Ensure selection updates once results load asynchronously.
  useEffect(() => {
    if (!selectedAssetId && resultEntries[0]) {
      setSelectedAssetId(resultEntries[0].assetId);
    }
  }, [resultEntries, selectedAssetId]);

  const activeAssetId = resultEntries.some((result) => result.assetId === selectedAssetId)
    ? selectedAssetId
    : resultEntries[0]?.assetId ?? null;
  const rawSelectedResult = (resultEntries.find((result) => result.assetId === activeAssetId) ??
    resultEntries[0] ??
    null);

  // Apply the Hazard Severity filter to the frames manifest.
  // ``frame_manifest`` entries don't carry severity directly — we
  // derive it from the count of hazard events on the same timestamp
  // in the parsed rawJson.
  const selectedResult = useMemo(() => {
    if (!rawSelectedResult || severityFilter === 'all') return rawSelectedResult;
    const frames = rawSelectedResult.frames?.frames ?? [];
    if (frames.length === 0) return rawSelectedResult;
    const hazardsByTs = new Map<number, Array<{ severity: string }>>();
    for (const h of rawSelectedResult.rawJson.hazards ?? []) {
      const bucket = Math.round(h.timestamp_sec * 1000 / 500) * 500;
      const entry = hazardsByTs.get(bucket) ?? [];
      entry.push({ severity: h.severity });
      hazardsByTs.set(bucket, entry);
    }
    const keep = (ts: number, sev: SeverityFilter): boolean => {
      if (sev === 'all') return true;
      const bucket = Math.round(ts / 500) * 500;
      const h = hazardsByTs.get(bucket) ?? [];
      return h.some((x) => x.severity === sev);
    };
    const filtered = frames.filter((f) => keep(f.timestamp_ms, severityFilter));
    return {
      ...rawSelectedResult,
      frames: rawSelectedResult.frames
        ? { ...rawSelectedResult.frames, frames: filtered, num_frames: filtered.length }
        : rawSelectedResult.frames,
    };
  }, [rawSelectedResult, severityFilter]);

  const visibleResults = useMemo(() => {
    if (resultEntries.length <= WINDOW_THRESHOLD) return resultEntries;
    return resultEntries.slice(0, Math.min(resultEntries.length, visibleCount));
  }, [resultEntries, visibleCount]);

  const handleDownloadJson = useCallback((filename: string, payload: unknown) => {
    const blob = new Blob([JSON.stringify(payload, null, 2)], { type: 'application/json' });
    const objectUrl = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = objectUrl;
    link.download = filename;
    link.click();
    URL.revokeObjectURL(objectUrl);
  }, []);

  const handleDownloadAllJson = () => {
    resultEntries.forEach((result) => {
      handleDownloadJson(result.assetName.replace(/\.[^.]+$/, '.json'), result.rawJson);
    });
  };

  const handleDownloadFramesZip = async () => {
    if (!selectedResult?.jobId) return;
    setZipBusy(true);
    try {
      const url = await downloadFramesZipUrl(selectedResult.jobId);
      const link = document.createElement('a');
      link.href = url;
      link.download = `${selectedResult.assetName.replace(/\.[^.]+$/, '')}_frames.zip`;
      link.click();
      URL.revokeObjectURL(url);
    } catch (e) {
      console.error('frames-zip download failed', e);
    } finally {
      setZipBusy(false);
    }
  };

  const onListScroll = () => {
    const el = listRef.current;
    if (!el) return;
    if (resultEntries.length <= WINDOW_THRESHOLD) return;
    if (el.scrollTop + el.clientHeight >= el.scrollHeight - 200) {
      setVisibleCount((n) => Math.min(resultEntries.length, n + WINDOW_PAGE_SIZE));
    }
  };

  // Loading state — show a friendly panel instead of a blank page or raw
  // backend JSON error.
  if (fetchState === 'loading') {
    return (
      <div className="min-h-screen overflow-hidden bg-[radial-gradient(circle_at_top,#163b84_0%,#08142e_34%,#020814_100%)] px-6 py-8 text-white lg:px-10">
        <div className="relative mx-auto max-w-7xl">
          <AppShellHeader />
          <div className="mt-16 rounded-2xl border border-cyan-500/20 bg-slate-950/78 p-8 text-center text-sm text-slate-300">
            <p className="text-base font-semibold text-white">Loading results…</p>
            <p className="mt-2 text-xs text-slate-400">
              Fetching the pipeline output from AWS. This usually takes a
              few seconds after the run completes.
            </p>
          </div>
        </div>
      </div>
    );
  }

  if (fetchState === 'error' && resultEntries.length === 0) {
    return (
      <div className="min-h-screen overflow-hidden bg-[radial-gradient(circle_at_top,#163b84_0%,#08142e_34%,#020814_100%)] px-6 py-8 text-white lg:px-10">
        <div className="relative mx-auto max-w-7xl">
          <AppShellHeader />
          <div className="mt-16 rounded-2xl border border-amber-500/30 bg-amber-500/5 p-8 text-center text-sm text-amber-100">
            <p className="text-base font-semibold text-white">
              We couldn&apos;t load this run&apos;s results.
            </p>
            <p className="mt-2 text-xs text-amber-200">
              The run might still be processing, or the result objects
              haven&apos;t been written to S3 yet. Check the History page
              for the latest job status.
            </p>
            <div className="mt-5 flex items-center justify-center gap-3">
              <button
                type="button"
                onClick={() => router.push('/history')}
                className="rounded-full border border-cyan-400/40 bg-cyan-500/10 px-4 py-2 text-xs font-semibold text-cyan-200 transition hover:bg-cyan-500/20"
              >
                Open History
              </button>
              <button
                type="button"
                onClick={() => router.push('/')}
                className="rounded-full bg-gradient-to-r from-cyan-500 via-blue-500 to-indigo-500 px-5 py-2 text-xs font-semibold text-white"
              >
                Start a new pipeline
              </button>
            </div>
          </div>
        </div>
      </div>
    );
  }

  const mode = activeMode ?? 'batch';

  return (
    <div className="min-h-screen overflow-hidden bg-[radial-gradient(circle_at_top,#163b84_0%,#08142e_34%,#020814_100%)] px-6 py-8 text-white lg:px-10">
      <div className="pointer-events-none absolute inset-0">
        <div className="absolute -left-16 top-12 h-56 w-56 rounded-full bg-cyan-400/18 blur-3xl" />
        <div className="absolute right-0 top-0 h-80 w-80 rounded-full bg-blue-500/20 blur-3xl" />
      </div>

      <div className="relative mx-auto max-w-7xl">
        {mode === 'batch' && showBatchCompletionPopup && isCurrentRun ? (
          <div className="fixed inset-0 z-50 flex items-center justify-center bg-slate-950/55 px-6">
            <div className="w-full max-w-lg rounded-2xl border border-cyan-500/20 bg-slate-950/90 p-8 shadow-xl">
              <h2 className="font-[family:var(--font-ibm-plex-sans)] text-2xl font-semibold text-white">
                Batch processing complete
              </h2>
              <p className="mt-3 text-sm leading-relaxed text-slate-300">
                Your batch results are ready. You can now review all annotated selected frames and inspect the JSON
                properties for each processed video.
              </p>
              <div className="mt-6 flex justify-end">
                <button
                  type="button"
                  onClick={() => setShowBatchCompletionPopup(false)}
                  className="rounded-full bg-gradient-to-r from-cyan-500 via-blue-500 to-indigo-500 px-6 py-2.5 text-sm font-semibold text-white shadow-[0_18px_40px_rgba(37,99,235,0.28)] transition hover:scale-[1.01]"
                >
                  Review results
                </button>
              </div>
            </div>
          </div>
        ) : null}

        <AppShellHeader
          rightContent={
            <div className="flex items-center gap-2">
              <button
                type="button"
                onClick={() => {
                  resetFlow();
                  router.push('/');
                }}
                className="rounded-lg border border-cyan-400/30 bg-cyan-500/10 px-4 py-1.5 text-sm font-semibold text-cyan-200 transition hover:bg-cyan-500/20"
              >
                Start new run
              </button>
              <button
                type="button"
                onClick={() => router.back()}
                className="rounded-lg border border-white/10 bg-white/5 px-4 py-1.5 text-sm font-medium text-slate-300 transition hover:bg-white/10 hover:text-white"
              >
                Back
              </button>
            </div>
          }
        />

        {state.notificationMessage ? (
          <div className="mt-4">
            <BatchNotification
              message={state.notificationMessage}
              permission={state.notificationPermission}
              onRequestPermission={requestNotificationPermission}
              onDismiss={() => setNotificationMessage(null)}
            />
          </div>
        ) : null}

        <div className="mt-8 flex flex-col gap-4 sm:flex-row sm:items-center sm:justify-between">
          <div>
            <h1 className="font-[family:var(--font-ibm-plex-sans)] text-[1.75rem] font-semibold tracking-tight text-white md:text-[2.25rem]">
              {mode === 'evaluation' ? 'Evaluation Results' : 'Detection Results'}
            </h1>
            <p className="mt-1 text-sm text-slate-400">
              {mode === 'evaluation'
                ? 'Benchmark metrics and GT coverage summary.'
                : 'Review annotated frames and inspect the structured JSON output.'}
            </p>
          </div>

          {mode === 'batch' ? (
            <div className="flex flex-wrap items-center gap-2">
              <button
                type="button"
                onClick={handleDownloadFramesZip}
                disabled={!selectedResult?.jobId || zipBusy}
                className="rounded-full border border-cyan-400/40 bg-cyan-500/10 px-4 py-2 text-xs font-semibold text-cyan-200 transition hover:bg-cyan-500/20 disabled:cursor-not-allowed disabled:opacity-50"
              >
                {zipBusy ? 'Building ZIP…' : 'Download frames ZIP'}
              </button>
              <button
                type="button"
                onClick={handleDownloadAllJson}
                className="rounded-full bg-gradient-to-r from-cyan-500 via-blue-500 to-indigo-500 px-5 py-2.5 text-sm font-semibold text-white shadow-[0_18px_40px_rgba(37,99,235,0.28)] transition hover:scale-[1.01]"
              >
                Download all JSON
              </button>
            </div>
          ) : null}
        </div>

        {mode === 'evaluation' ? (
          <div className="mt-6">
            <EvaluationReportResults />
          </div>
        ) : (
          <div className="mt-6 grid gap-6 2xl:grid-cols-[0.26fr_0.74fr]">
            <aside
              ref={listRef}
              onScroll={onListScroll}
              className="max-h-[70vh] overflow-y-auto rounded-2xl border border-cyan-500/20 bg-slate-950/78 p-5 backdrop-blur"
            >
              <div className="flex items-center justify-between">
                <h2 className="text-sm font-semibold text-white">
                  {mode === 'batch' ? 'Batch results' : 'Processed file'}
                </h2>
                <span className="rounded-md bg-white/10 px-2 py-0.5 text-[11px] font-semibold text-slate-300">
                  {visibleResults.length}/{resultEntries.length}
                </span>
              </div>

              <div className="mt-4 space-y-2">
                {visibleResults.map((result) => {
                  const isSelected = result.assetId === selectedResult?.assetId;
                  const r = result as EnrichedAssetResult;
                  const frameCount = r.frames?.num_frames ?? r.rawJson.frames.length;

                  return (
                    <button
                      key={result.assetId}
                      type="button"
                      data-test-id={`result-asset-${result.assetId}`}
                      onClick={() => setSelectedAssetId(result.assetId)}
                      className={`w-full rounded-xl border p-3.5 text-left transition ${
                        isSelected
                          ? 'border-cyan-400/50 bg-cyan-500/10'
                          : 'border-slate-700/60 bg-slate-900/50 hover:border-cyan-400/40 hover:bg-slate-900'
                      }`}
                    >
                      <p className="truncate text-sm font-medium text-white">{result.assetName}</p>
                      <p className="mt-0.5 text-[11px] uppercase tracking-wider text-slate-500">{result.kind}</p>
                      <p className="mt-2 text-xs text-slate-400">
                        {frameCount} frame{frameCount === 1 ? '' : 's'} ·{' '}
                        {result.rawJson.hazards.length} hazard{result.rawJson.hazards.length === 1 ? '' : 's'}
                      </p>
                    </button>
                  );
                })}
                {visibleResults.length < resultEntries.length ? (
                  <p className="pt-2 text-center text-[11px] text-slate-500">
                    Scroll to load more…
                  </p>
                ) : null}
              </div>
            </aside>

            <div className="space-y-6">
              {mode === 'batch' && isCurrentRun ? (
                <div className="rounded-xl border border-emerald-500/20 bg-emerald-500/5 px-4 py-3 text-sm font-medium text-emerald-200">
                  Batch processing finished. Select any processed file from the left to inspect its frames and JSON output.
                </div>
              ) : null}

              <div className="flex flex-wrap items-center justify-between gap-3">
                <div className="flex items-center gap-1 rounded-full border border-white/10 bg-white/5 p-1 w-fit">
                  {(
                    [
                      { id: 'frames', label: 'Frames' },
                      { id: 'rendered', label: 'Rendered' },
                      { id: 'json', label: 'JSON' },
                    ] as Array<{ id: ResultTab; label: string }>
                  ).map((tab) => (
                    <button
                      key={tab.id}
                      type="button"
                      data-test-id={`results-tab-${tab.id}`}
                      onClick={() => setActiveTab(tab.id)}
                      className={`rounded-full px-4 py-1.5 text-xs font-semibold transition ${
                        activeTab === tab.id
                          ? 'bg-cyan-500/20 text-cyan-100'
                          : 'text-slate-300 hover:text-white'
                      }`}
                    >
                      {tab.label}
                    </button>
                  ))}
                </div>
                {activeTab === 'frames' ? (
                  <div className="flex items-center gap-2 text-[11px]">
                    <span className="font-semibold uppercase tracking-[0.18em] text-slate-500">
                      Filter
                    </span>
                    {(
                      [
                        { id: 'all', label: 'All' },
                        { id: 'high', label: 'High' },
                        { id: 'medium', label: 'Medium' },
                        { id: 'low', label: 'Low' },
                      ] as Array<{ id: SeverityFilter; label: string }>
                    ).map((opt) => (
                      <button
                        key={opt.id}
                        type="button"
                        onClick={() => setSeverityFilter(opt.id)}
                        className={`rounded-full border px-3 py-1 font-semibold transition ${
                          severityFilter === opt.id
                            ? 'border-cyan-400 bg-cyan-500/10 text-cyan-200'
                            : 'border-white/10 bg-white/5 text-slate-400 hover:border-cyan-400/40 hover:text-slate-200'
                        }`}
                      >
                        {opt.label}
                      </button>
                    ))}
                  </div>
                ) : null}
              </div>
              <details className="rounded-2xl border border-white/10 bg-slate-950/50 p-4 text-xs">
                <summary className="cursor-pointer font-semibold text-slate-200">
                  Export selected result to S3
                </summary>
                <div className="mt-3 flex flex-col gap-2">
                  <input
                    type="text"
                    value={exportPath}
                    onChange={(e) => setExportPath(e.target.value)}
                    className="w-full rounded-lg border border-slate-700 bg-slate-950/75 px-3 py-2 text-sm text-white outline-none focus:border-cyan-400"
                    placeholder="s3://bucket/prefix"
                  />
                  <p className="text-[10px] text-slate-500">
                    We&apos;ll copy the selected run&apos;s
                    {' '}<code className="rounded bg-black/40 px-1">output.json</code>,
                    {' '}<code className="rounded bg-black/40 px-1">frames_manifest.json</code>,
                    and <code className="rounded bg-black/40 px-1">frames/*</code> to this prefix.
                    {exportPath === DEFAULT_EXPORT_PATH
                      ? ' This is the default location the pipeline already writes to — so "Export" here is a no-op.'
                      : ''}
                  </p>
                  <div className="flex items-center gap-2">
                    <button
                      type="button"
                      disabled={!selectedResult?.jobId || exportBusy || !exportPath.startsWith('s3://')}
                      onClick={async () => {
                        if (!selectedResult?.jobId) return;
                        setExportBusy(true);
                        setExportMessage(null);
                        try {
                          // Free-tier: we let the user download a ZIP
                          // and upload wherever they like — the backend
                          // doesn't expose a "copy results to arbitrary
                          // S3 bucket" endpoint for IAM reasons.
                          const url = await downloadFramesZipUrl(selectedResult.jobId);
                          const link = document.createElement('a');
                          link.href = url;
                          link.download = `${selectedResult.assetName.replace(/\.[^.]+$/, '')}_export.zip`;
                          link.click();
                          URL.revokeObjectURL(url);
                          setExportMessage('Downloaded — upload to your S3 prefix manually.');
                        } catch (e) {
                          setExportMessage(`Export failed: ${e instanceof Error ? e.message : String(e)}`);
                        } finally {
                          setExportBusy(false);
                        }
                      }}
                      className="rounded-full border border-cyan-400/40 bg-cyan-500/10 px-4 py-1.5 text-xs font-semibold text-cyan-200 transition hover:bg-cyan-500/20 disabled:cursor-not-allowed disabled:opacity-50"
                    >
                      {exportBusy ? 'Exporting…' : 'Download export ZIP'}
                    </button>
                    {exportMessage ? (
                      <span className="text-[11px] text-slate-400">{exportMessage}</span>
                    ) : null}
                  </div>
                </div>
              </details>

              {activeTab === 'frames' ? (
                <BackendFrameGallery
                  result={selectedResult}
                  onDownloadJson={handleDownloadJson}
                />
              ) : null}

              {activeTab === 'rendered' ? (
                <div className="grid gap-6">
                  {selectedResult ? (
                    <ResultsPropertiesPanel result={selectedResult} />
                  ) : null}
                </div>
              ) : null}

              {activeTab === 'json' ? (
                <div className="rounded-2xl border border-cyan-500/20 bg-slate-950/78 p-5">
                  <div className="mb-3 flex items-center justify-between">
                    <h3 className="text-sm font-semibold text-white">
                      Full output JSON
                    </h3>
                    {selectedResult ? (
                      <button
                        type="button"
                        onClick={() =>
                          handleDownloadJson(
                            `${selectedResult.assetName.replace(/\.[^.]+$/, '')}.json`,
                            selectedResult.rawJson,
                          )
                        }
                        className="rounded-full border border-cyan-400/40 bg-cyan-500/10 px-3 py-1 text-[11px] font-semibold text-cyan-200 hover:bg-cyan-500/20"
                      >
                        Download
                      </button>
                    ) : null}
                  </div>
                  <pre className="max-h-[70vh] overflow-auto rounded-lg bg-slate-900/80 p-4 text-[11px] leading-relaxed text-slate-200">
                    {JSON.stringify(selectedResult?.rawJson ?? {}, null, 2)}
                  </pre>
                </div>
              ) : null}
            </div>
          </div>
        )}
      </div>
    </div>
  );
}
